package com.example.mybottom;

import android.graphics.drawable.Drawable;

public class MyInterestRecyclerItem {

    private int regionDrawable ;
    private String homeNum ;
    private String title ;
    private String subTitle ;


    public int getRegionDrawable() {
        return regionDrawable;
    }

    public void setRegionDrawable(int regionDrawable) {
        this.regionDrawable = regionDrawable;
    }

    public String getHomeNum() {
        return homeNum;
    }

    public void setHomeNum(String homeNum) {
        this.homeNum = homeNum;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getSubTitle() {
        return subTitle;
    }

    public void setSubTitle(String subTitle) {
        this.subTitle = subTitle;
    }
}
